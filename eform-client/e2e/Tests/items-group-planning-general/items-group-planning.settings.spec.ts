import loginPage from '../../Page objects/Login.page';
import itemsGroupPlanningSettingsPage from '../../Page objects/ItemsGroupPlanning/ItemsGroupPlanningSettings.page';

const expect = require('chai').expect;

describe('Items group planning plugin settings page', function () {
  before(async () => {
    await loginPage.open('/auth');
    await loginPage.login();
    await itemsGroupPlanningSettingsPage.goToSettingsPage();
  });
  it('save items planning settings', async () => {
    const settingsData = {
      sdkConnectionString:
        'Server=SQLEXPRESS;Database=123_SDK;User ID=sa;Password=Qq1234567$;',
      logLevel: '4',
      logLimit: '25000',
      maxParallelism: '1',
      numberOfWorkers: '1',
      siteIds: '1,2,3',
    };
    await itemsGroupPlanningSettingsPage.saveSettings(settingsData);
    await $('#spinner-animation').waitForDisplayed({
      timeout: 90000,
      reverse: true,
    });
    // Check that items planning settings saved correctly
    const savedSettings = itemsGroupPlanningSettingsPage.getSettings();
    expect(
      savedSettings.sdkConnectionString,
      'SDK connection string is incorrect'
    ).equal(settingsData.sdkConnectionString);
    expect(savedSettings.logLevel, 'Log Level is incorrect').equal(
      settingsData.logLevel
    );
    expect(savedSettings.logLimit, 'Log Limit is incorrect').equal(
      settingsData.logLimit
    );
    expect(savedSettings.maxParallelism, 'Max parallelism is incorrect').equal(
      settingsData.maxParallelism
    );
    expect(
      savedSettings.numberOfWorkers,
      'Number of workers is incorrect'
    ).equal(settingsData.numberOfWorkers);
    expect(savedSettings.siteIds, 'Site ids is incorrect').equal(
      settingsData.siteIds
    );
  });
});
